package entity.enums;

/**
 * Enum representing the status of a replenishment request.
 */
public enum ReplenishmentStatus {

    /**
     * Replenishment request is pending approval or action.
     */
    PENDING,

    /**
     * Replenishment request has been approved.
     */
    APPROVED,

    /**
     * Replenishment request has been rejected.
     */
    REJECTED
}

