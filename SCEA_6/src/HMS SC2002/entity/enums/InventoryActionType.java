package entity.enums;

/**
 * Enum representing different types of inventory actions.
 */
public enum InventoryActionType {

    /**
     * Action to add items to the inventory.
     */
    ADDITION,

    /**
     * Action to reduce the number of items in the inventory.
     */
    REDUCTION,

    /**
     * Action to replenish the stock of items in the inventory.
     */
    REPLENISHMENT
}
